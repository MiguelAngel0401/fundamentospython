# Operadores booleanos
print("Operadores Booleanos")
# < <=
print(10<10)
print(10<=10)
# > >=
print(10>10)
print(10>=10)
# ==
print(10==10)
# !=
print(10!=10)

#Operadores logicos
print("Operadores Logicos")
# and (True if all true)
print(True and True)
print(True and False)

#or (True if at least one True)
print(False or False)
print(False or True)

# not true
print(not True)
print(not False)